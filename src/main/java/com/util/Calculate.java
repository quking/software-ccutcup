package com.util;

import java.math.BigDecimal;

/**
 * Created by mtl on 17-5-2.
 */
public class Calculate {

    public static BigDecimal add(BigDecimal a,BigDecimal b)
    {

        return a.add(b);

    }

    public static BigDecimal sub(BigDecimal a,BigDecimal b)
    {

        return a.subtract(b);

    }

}
